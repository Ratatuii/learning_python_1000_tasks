a, b = int(input('Введите не отрицательное число a: ')), int(input('Введите не отрицательное число b: '))
a *= a
b *= b
print('Сумма квадратов чисел {} и {} = {}'.format(a, b, a + b))
print('Разность квадратов чисел {} и {} = {}'.format(a, b, a - b))
print('Произведение квадратов чисел {} и {} = {}'.format(a, b, a * b))
print('Частное квадратов чисел {} и {} = {}'.format(a, b, a / b))
